package ex_07_Increment_Decrement;

public class Lab02_Decrement_Op {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(++a);
        System.out.println(a);


        // Post increment  = print first and then increase
        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
