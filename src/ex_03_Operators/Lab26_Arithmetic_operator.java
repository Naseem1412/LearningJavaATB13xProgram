package ex_03_Operators;

public class Lab26_Arithmetic_operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        float c = 3.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);
        System.out.println("a+b");

    }

}
